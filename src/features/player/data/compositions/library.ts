// 全曲コンポジション定義 — 柔らかい音色のみ使用（piano/pad/upright/perc）
// lead(sawtooth), bass(square), guitar(square) は使用しない
import { Composition, midiToHz, Part } from "./types";

const N = (t: number, d: number, v: number, midi: number) => ({ t, d, v, p: midiToHz(midi) });
type NR = ReturnType<typeof N>;

// --- ヘルパー ---

/** パッドコード（sine波 = 最も柔らかい音） */
function chordPad(chords: number[][], bpc: number, v = 0.06): Part {
  return { instrument: "pad", notes: chords.flatMap((c, i) => c.map(m => N(i * bpc, bpc, v, m))) };
}

/** ピアノアルペジオ（triangle + sine倍音 = 温かみのある音） */
function pianoArp(chords: number[][], bpc: number, pattern: number[], v = 0.04, d = 0.4): Part {
  const notes: NR[] = [];
  chords.forEach((ch, ci) => {
    const s = [...ch].sort((a, b) => a - b);
    pattern.forEach((p, pi) => {
      notes.push(N(ci * bpc + pi * (bpc / pattern.length), d, v, s[p % s.length] + (p >= s.length ? 12 : 0)));
    });
  });
  return { instrument: "piano", notes };
}

/** ウッドベース（lowpass filtered = 丸い音、音割れしない） */
function uprightBass(roots: number[], bpc: number, pattern: number[][], v = 0.055): Part {
  const notes: NR[] = [];
  roots.forEach((r, i) => pattern.forEach(([off, dur]) => notes.push(N(i * bpc + off, dur, v, r))));
  return { instrument: "upright", notes };
}

/** ウォーキングベース */
function walkBass(roots: number[], bpc: number, v = 0.05): Part {
  const steps = [0, 2, 4, 5, 7, 5, 3, 2];
  const notes: NR[] = [];
  roots.forEach((r, i) => {
    for (let b = 0; b < bpc; b++) notes.push(N(i * bpc + b, 0.85, v, r + steps[(i * bpc + b) % steps.length]));
  });
  return { instrument: "upright", notes };
}

/** ピアノメロディ */
function pianoMel(m: [number, number, number][], v = 0.05): Part {
  return { instrument: "piano", notes: m.map(([t, d, p]) => N(t, d, v, p)) };
}

/** パーカッション（sine波 = 柔らかいクリック、音割れなし） */
function percKit(bars: number, style: "four" | "pop" | "brush" | "lofi" | "gentle", v = 0.035): Part {
  const notes: NR[] = [];
  for (let b = 0; b < bars; b++) {
    const t = b * 4;
    if (style === "four") {
      // EDM: ソフトキック + オフビートハット
      for (let i = 0; i < 4; i++) notes.push(N(t + i, 0.06, v, 60)); // kick: MIDI 60 = 中周波数、割れない
      for (let i = 0; i < 8; i++) notes.push(N(t + i * 0.5 + 0.25, 0.03, v * 0.6, 96));
    } else if (style === "pop") {
      notes.push(N(t, 0.06, v, 60), N(t + 2, 0.06, v, 60)); // kick
      notes.push(N(t + 1, 0.04, v * 0.8, 96), N(t + 3, 0.04, v * 0.8, 96)); // snare (高めMIDI)
      for (let i = 0; i < 8; i++) notes.push(N(t + i * 0.5, 0.03, v * 0.45, 92));
    } else if (style === "brush") {
      notes.push(N(t, 0.05, v * 0.6, 62), N(t + 2, 0.05, v * 0.4, 62));
      notes.push(N(t, 0.04, v * 0.45, 92), N(t + 1, 0.04, v * 0.35, 92));
      notes.push(N(t + 1.67, 0.03, v * 0.5, 95), N(t + 2, 0.04, v * 0.45, 92));
      notes.push(N(t + 3, 0.04, v * 0.35, 92), N(t + 3.67, 0.03, v * 0.5, 95));
    } else if (style === "lofi") {
      notes.push(N(t, 0.05, v * 0.6, 62), N(t + 2, 0.05, v * 0.4, 62));
      notes.push(N(t + 1, 0.03, v * 0.4, 96), N(t + 3, 0.03, v * 0.4, 96));
      for (let i = 0; i < 16; i++) notes.push(N(t + i * 0.25, 0.02, v * (i % 4 === 0 ? 0.4 : 0.22), 98));
    } else { // gentle
      notes.push(N(t, 0.06, v * 0.3, 62));
      notes.push(N(t, 0.03, v * 0.25, 92), N(t + 2, 0.03, v * 0.25, 92));
    }
  }
  return { instrument: "perc", notes };
}

