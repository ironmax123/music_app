import { Composition, midiToHz, Part } from "./types";

// --- ヘルパー関数 ---
const N = (t: number, d: number, v: number, midi: number) => ({ t, d, v, p: midiToHz(midi) });

function padPartFromChords(chords: number[][], beatsPerChord: number, velocity = 0.085, startBeat = 0): Part {
  const notes = chords.flatMap((pitches, idx) =>
    pitches.map((midi) => N(startBeat + idx * beatsPerChord, beatsPerChord, velocity, midi)),
  );
  return { instrument: "pad", notes };
}

function hatPart(totalBeats: number, stepBeats = 0.5, startBeat = 0, midi = 96, velocity = 0.05, durBeats = 0.08): Part {
  const notes: ReturnType<typeof N>[] = [];
  for (let t = startBeat; t < startBeat + totalBeats; t += stepBeats) {
    notes.push(N(t, durBeats, velocity, midi));
  }
  return { instrument: "perc", notes };
}

function dyadCompFromChords(
  instrument: Part["instrument"], chords: number[][], beatsPerChord: number,
  offsets: number[], velocity = 0.06, sustainBeats = 0.4, startBeat = 0,
): Part {
  const notes: ReturnType<typeof N>[] = [];
  chords.forEach((voicing, idx) => {
    const sorted = [...voicing].sort((a, b) => a - b);
    const a = sorted[sorted.length - 2] ?? sorted[0];
    const b = sorted[sorted.length - 1] ?? sorted[0];
    const base = startBeat + idx * beatsPerChord;
    for (const off of offsets) {
      notes.push(N(base + off, sustainBeats, velocity, a));
      notes.push(N(base + off, sustainBeats, velocity, b));
    }
  });
  return { instrument, notes };
}

function kickPart(bars: number, startBeat = 0, midi = 52, velocity = 0.045, durBeats = 0.08): Part {
  const notes: ReturnType<typeof N>[] = [];
  for (let bar = 0; bar < bars; bar++) {
    const base = startBeat + bar * 4;
    notes.push(N(base, durBeats, velocity, midi));
    notes.push(N(base + 2, durBeats, velocity, midi));
  }
  return { instrument: "perc", notes };
}

function snarePart(bars: number, startBeat = 0, midi = 100, velocity = 0.038, durBeats = 0.06): Part {
  const notes: ReturnType<typeof N>[] = [];
  for (let bar = 0; bar < bars; bar++) {
    const base = startBeat + bar * 4;
    notes.push(N(base + 1, durBeats, velocity, midi));
    notes.push(N(base + 3, durBeats, velocity, midi));
  }
  return { instrument: "perc", notes };
}

// ベースライン生成: 各コードのルート音をパターンで演奏
function bassLine(roots: number[], beatsPerChord: number, pattern: number[], vel = 0.08, dur = 0.3): Part {
  const notes: ReturnType<typeof N>[] = [];
  roots.forEach((root, idx) => {
    const base = idx * beatsPerChord;
    for (const off of pattern) {
      notes.push(N(base + off, dur, vel, root));
    }
  });
  return { instrument: "bass", notes };
}

// メロディ生成ヘルパー
function melodyLine(instrument: Part["instrument"], melody: [number, number, number][], vel = 0.07): Part {
  // melody: [startBeat, duration, midiNote]
  return { instrument, notes: melody.map(([t, d, m]) => N(t, d, vel, m)) };
}

